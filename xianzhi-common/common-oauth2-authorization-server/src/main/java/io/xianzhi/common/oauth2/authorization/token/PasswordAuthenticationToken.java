package io.xianzhi.common.oauth2.authorization.token;

import org.springframework.security.core.Authentication;
import org.springframework.security.oauth2.core.AuthorizationGrantType;

import java.util.Map;
import java.util.Set;

/**
 * 密码模式token
 *
 * @author Max
 * @since 1.0.0
 */
public class PasswordAuthenticationToken extends AbstractBaseAuthenticationToken {
    /**
     * 构造
     *
     * @param authorizationGrantType 授权类型
     * @param clientPrincipal        客户端信息
     * @param scopes                 作用域
     * @param additionalParameters   其他扩展参数
     */
    public PasswordAuthenticationToken(AuthorizationGrantType authorizationGrantType, Authentication clientPrincipal, Set<String> scopes, Map<String, Object> additionalParameters) {
        super(authorizationGrantType, clientPrincipal, scopes, additionalParameters);
    }
}
